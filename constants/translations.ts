export type TranslationKey = keyof typeof translations.english;

export const languageDisplayNames: Record<AppLanguage, string> = {
  english: "English",
  taglish: "Taglish",
};

export function translate(
  language: AppLanguage,
  key: TranslationKey,
  params?: Record<string, number | string>,
) {
  const template = translations[language][key] ?? translations.taglish[key];

  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(params?.[token] ?? ""));
}
